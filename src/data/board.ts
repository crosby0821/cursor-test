export function isPropertyTile(
  tile: BoardTile
): tile is import('../game/types').PropertyTile {
  return tile.kind === 'property'
}

export function isOwnableTile(
  tile: BoardTile
): tile is
  | import('../game/types').PropertyTile
  | import('../game/types').RailroadTile
  | import('../game/types').UtilityTile {
  return (
    tile.kind === 'property' ||
    tile.kind === 'railroad' ||
    tile.kind === 'utility'
  )
}
